question = '''Starting with mRNA leaving the nucleus, list and describe four major steps involved in protein synthesis.'''

gt = '''1. mRNA exits nucleus via nuclear pore.
2. mRNA travels through the cytoplasm to the ribosome or enters the rough endoplasmic reticulum.
3. mRNA bases are read in triplets called codons (by rRNA).
4. tRNA carrying the complementary (U=A, C+G) anticodon recognizes the complementary codon of the mRNA.
5. The corresponding amino acids on the other end of the tRNA are bonded to adjacent tRNA's amino acids.
6. A new corresponding amino acid is added to the tRNA.
7. Amino acids are linked together to make a protein beginning with a START codon in the P site (initiation).
8. Amino acids continue to be linked until a STOP codon is read on the mRNA in the A site (elongation and termination).'''

